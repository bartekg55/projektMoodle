<legend>Treść zadania</legend>
<p>Celem projektu jest stworzenie prostego systemu e-lerningowego. Projekt powinien umożliwiać rejestrowanie trzech typów użytkowników:
<ul>
    <li> administrator </li>
    <li> nauczyciel</li>
    <li> student</li>
</ul>
</p>
<p>Administrator powinien mieć możliwość zarządzania wszystkimi, kursami i użytkownikami aplikacji. </p>

<p>Każdy użytkownik, który rejestruje się do aplikacji powinien zostać zarejestrowany jako student. Administrator może zmienić konto studenta na konto nauczyciela. </p>

<p>Nauczyciel ma możliwość tworzenia kursów. W obrębie własnego kursu nauczyciel ma możliwość zapisywania użytkowników, dodawania plików do lekcji, tworzenia zadań, umożliwiających pobranie pliku od użytkownika lub bezpośredniej treści wpisanej na stronie zadania (podobnie jak w moodle).</p>

<p>Student powinien mieć możliwość wejścia tylko do kursów, na które jest zapisany. Studenta może zapisać tylko administrator lub student może dokonać tego samodzielnie na podstawie kodu, który otrzyma od nauczyciela. </p>

